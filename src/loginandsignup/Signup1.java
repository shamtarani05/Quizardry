    private void SignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SignupActionPerformed
      if (Email.getText().trim().isEmpty())
        {
           JOptionPane.showMessageDialog(null, " Please Fill Email"); 
        }
        
       else if (Username.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, " Please Fill username"); 
           
        }
        else if((password.getText().isEmpty() ))
        {
             JOptionPane.showMessageDialog(null, " Please Fill Password");            
        }
         else if((recpassword.getText().isEmpty() ))
        {
             JOptionPane.showMessageDialog(null, " Please Fill Recovery Password");            
        }
        else
        {
            try {
                Admin.signup(Username.getText(), Email.getText(),password.getText(),recpassword.getText());
            } catch (SQLException ex) {
                Logger.getLogger(Signup1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_SignupActionPerformed
