    private void SigninActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SigninActionPerformed
         if (userinput.getText().trim().isEmpty()  ) {
            JOptionPane.showMessageDialog(null, "Please fill username");

        }
        else if (Admin.getText().trim().isEmpty()  ) {
            JOptionPane.showMessageDialog(null, "Please fill Adminusername");

        }
         else if ((password.getText() == null ))
         {
             JOptionPane.showMessageDialog(null, "Please fill Password");  
         }
        else
        {
            studentlogin currentInstance = this;
            user.StudentLogin(userinput.getText(),password.getText(),currentInstance,Admin.getText());
        }
    }//GEN-LAST:event_SigninActionPerformed

